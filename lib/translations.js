// Helper function to translate text to Chinese
function translateToChinese(text) {
  if (!text) return '';
  
  const textStr = String(text).toLowerCase();
  
  // First try exact match
  if (translations[textStr]) {
    return translations[textStr];
  }
  
  // Try to find partial matches (for compound names like "diamond_sword")
  const parts = textStr.split('_');
  let translatedParts = [];
  
  for (const part of parts) {
    if (translations[part]) {
      translatedParts.push(translations[part]);
    } else {
      translatedParts.push(part);
    }
  }
  
  // Join translated parts (Chinese doesn't use underscores)
  return translatedParts.join('');
}

// Helper function to translate an object with string values
function translateObject(obj) {
  if (!obj || typeof obj !== 'object') return obj;
  
  const result = {};
  for (const [key, value] of Object.entries(obj)) {
    if (typeof value === 'string') {
      result[key] = translateToChinese(value);
    } else if (Array.isArray(value)) {
      result[key] = value.map(item => {
        if (typeof item === 'string') {
          return translateToChinese(item);
        } else if (item && typeof item === 'object' && item.name) {
          // Handle inventory/item objects
          const translatedItem = { ...item };
          translatedItem.name = translateToChinese(item.name);
          return translatedItem;
        }
        return item;
      });
    } else if (value && typeof value === 'object' && value.name) {
      // Handle objects with a name property (like armor pieces)
      result[key] = { ...value };
      result[key].name = translateToChinese(value.name);
    } else {
      result[key] = value;
    }
  }
  return result;
}

// Helper to translate entity information
function translateEntity(entity) {
  if (!entity || typeof entity !== 'object') return entity;
  
  const translated = { ...entity };
  
  if (entity.type) {
    translated.type = translateToChinese(entity.type);
  }
  
  if (entity.displayName) {
    translated.displayName = translateToChinese(entity.displayName);
  }
  
  if (entity.category) {
    translated.category = translateToChinese(entity.category);
  }
  
  return translated;
}

